#todo: Дана сторона квадрата a. Найти его площадь S = a²
# Примечание: сторону квадрата получаем через функцию input().

a = int(input("введите сторону квадрата a "))
print ( "площадь квадрата s =", a**2 )
b = int(input("введите еще одну сторону фигуры b "))
print ( "площадь прямоугольника s =", a*b )
